public class TPC02 {
	public static void main(String[] args) {
       // 프로그래밍의 3대요소 : 변수, 자료형(DataType), 할당(=)
	   // 1+1=2	
	   int a,b,c;
	   a=1;
	   b=1;
	   c=a+b; // 2
	   System.out.println(c);
	   
	   float f; // float, double
	   f=34.5f;
	   System.out.println(f);
	   
	   char d;
	   d='A';
	   System.out.println(d);
	   
	   boolean g;
	   g=true; // false(거짓), true(참)
	   
	   System.out.println(g);
	   
	   //Book bk;
	   
	   
	}
}
